package zpl.oj.model.common;

public class Challenge {
    private int challengeId;
	private String challengeName;
	private String challengeCompany;
	private String challengeTime;
	private int challengeScore;
	private int challengeRank;
	public int getChallengeId() {
		return challengeId;
	}
	public void setChallengeId(int challengeId) {
		this.challengeId = challengeId;
	}
	public String getChallengeName() {
		return challengeName;
	}
	public void setChallengeName(String challengeName) {
		this.challengeName = challengeName;
	}
	public String getChallengeCompany() {
		return challengeCompany;
	}
	public void setChallengeCompany(String challengeCompany) {
		this.challengeCompany = challengeCompany;
	}
	public String getChallengeTime() {
		return challengeTime;
	}
	public void setChallengeTime(String challengeTime) {
		this.challengeTime = challengeTime;
	}
	public int getChallengeScore() {
		return challengeScore;
	}
	public void setChallengeScore(int i) {
		this.challengeScore = i;
	}
	public int getChallengeRank() {
		return challengeRank;
	}
	public void setChallengeRank(int challengeRank) {
		this.challengeRank = challengeRank;
	}


    
	
	
}
